package herbert_schildt.chapter_4;
// Продемонстрировать применение операции %
public class Modulus {
    public static void main(String[] args) {
        int x = 52;
        double y = 4546.879;

        System.out.println("x mod 8 = " + x % 8);
        System.out.println("y mod 7 = " + y % 7);
    }
}
